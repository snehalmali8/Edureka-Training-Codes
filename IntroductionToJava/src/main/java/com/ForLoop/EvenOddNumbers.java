package com.ForLoop;

public class EvenOddNumbers {
	 public static void main(String[] args) {
	        System.out.println("First 10 even numbers:");
	        for (int i = 2; i <= 20; i += 2) {
	            System.out.print(i + " ");
	        }
	        System.out.println();

	        System.out.println("First 10 odd numbers:");
	        for (int i = 1; i <= 19; i += 2) {
	            System.out.print(i + " ");
	        }
	        System.out.println();
	    }
}
